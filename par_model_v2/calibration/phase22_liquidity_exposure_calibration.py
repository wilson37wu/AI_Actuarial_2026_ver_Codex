# 4. Calibrated 7x7 + PSD validation
# ---------------------------------------------------------------------------

def build_calibrated_correlation(spec: Dict[str, Any],
                                 estimated: Dict[str, float]
                                 ) -> Tuple[SevenDriverCorrelation, Any]:
    """SevenDriverCorrelation with the ESTIMATED couplings; PSD-validated (no repair)."""
    est = {k: round(float(estimated[k]), 4) for k in COUPLING_KEYS}
    corr = SevenDriverCorrelation(
        liq_rate=est["liq_rate"], liq_equity=est["liq_equity"],
        liq_spread=est["liq_spread"], liq_lapse=est["liq_lapse"],
        liq_mortality=est["liq_mortality"], liq_fx=est["liq_fx"],
    )
    C = corr.matrix(float(spec["gbm_rate_equity_corr"]))
    C_t = tuple(tuple(float(v) for v in row) for row in C)
    report = CorrelationMatrixValidator().validate_matrix(C_t, repair=False)
    return corr, report


# ---------------------------------------------------------------------------
# 5. Var-covar sensitivity (Phase 21 Task 4 persisted standalone SCRs)
# ---------------------------------------------------------------------------

def var_covar_sensitivity(spec: Dict[str, Any],
                          corr: SevenDriverCorrelation,
                          exposure_notional: float,
                          task4_report_path: Optional[Path] = None
                          ) -> Dict[str, Any]:
    """SCR sensitivity across the notional grid and a coupling perturbation.

    Uses the persisted seven-driver standalone SCRs; the liquidity standalone
    SCR scales linearly in the exposure notional (the liability impact is
    linear in the notional).  Full nested/copula re-aggregation is Task 4.
    """
    p = Path(task4_report_path) if task4_report_path is not None else _TASK4_REPORT
    agg = json.loads(p.read_text(encoding="utf-8"))["aggregation"]
    scr = dict(agg["standalone_scr"])
    base_notional = float(agg["liquidity_exposure_notional"])
    sens = spec["sensitivity"]
    re_corr = float(spec["gbm_rate_equity_corr"])

    def vc(C: np.ndarray, notional: float) -> float:
        s = dict(scr)
        s["liquidity"] = scr["liquidity"] * notional / base_notional
        v = np.array([s[k] for k in DRIVERS_7D], dtype=float)
        return float(np.sqrt(max(0.0, float(v @ C @ v))))

    C_cal = corr.matrix(re_corr)
    C_pl = SevenDriverCorrelation().matrix(re_corr)
    grid = [float(m) for m in sens["notional_multipliers"]]
    scr_grid = [vc(C_cal, exposure_notional * m) for m in grid]

    eps = float(sens["coupling_perturbation"])
    pert = {}
    for key in ("liq_spread", "liq_equity"):
        for sgn in (+1.0, -1.0):
            kw = {k: getattr(corr, k) for k in COUPLING_KEYS}
            kw[key] = float(np.clip(kw[key] + sgn * eps, -0.99, 0.99))
            C_p = SevenDriverCorrelation(**kw).matrix(re_corr)
            pert["{}{:+.2f}".format(key, sgn * eps)] = vc(C_p, exposure_notional)

    base_cal = vc(C_cal, exposure_notional)
    base_pl = vc(C_pl, base_notional)
    rel_change = abs(base_cal - base_pl) / base_pl if base_pl > 0 else float("inf")
    monotone = all(scr_grid[i] < scr_grid[i + 1] for i in range(len(scr_grid) - 1))
    grid_spread_rel = ((max(scr_grid) - min(scr_grid)) / base_cal) if base_cal > 0 else float("inf")
    # Net liquidity cross-term: sum_j C[6,j] * scr_j (j != liquidity). Negative
    # => the liquidity driver is NET-DIVERSIFYING at this scale, so var-covar
    # SCR can legitimately DECREASE as the notional rises (documented finding;
    # monotonicity is therefore reported but NOT gated).
    v6 = np.array([scr[k] for k in DRIVERS_7D[:6]], dtype=float)
    net_cross = float(C_cal[6, :6] @ v6)
    pert_rel_max = max(abs(v - base_cal) / base_cal for v in pert.values()) if pert else 0.0
    return {
        "var_covar_scr_placeholder": base_pl,
        "var_covar_scr_calibrated": base_cal,
        "calibrated_vs_placeholder_rel_change": rel_change,
        "notional_multipliers": grid,
        "var_covar_scr_grid": scr_grid,
        "monotone_in_notional": bool(monotone),
        "grid_spread_rel": grid_spread_rel,
        "net_liquidity_cross_term": net_cross,
        "liquidity_net_diversifying": bool(net_cross < 0.0),
        "coupling_perturbations": pert,
        "max_perturbation_rel_change": pert_rel_max,
        "max_allowed_rel_change": float(sens["max_var_covar_scr_change_rel"]),
        "standalone_scr_source": str(p),
    }


