def _refresh_risk(store: GovernanceStore, risk_id: str,
                  gate: ProductionGateStatus,
                  exposure: Dict[str, float],
                  estimated: Dict[str, float], ts: str) -> str:
    try:
        entry = store.risk_register.get(risk_id)
    except KeyError:
        return "NOT_FOUND"
    if gate.status != "PASS":
        return entry.mitigation_status.value
    note = (
        "Phase 22 Task 3 (G-LIQX): liquidity exposure notional and 7x7 liquidity couplings "
        "calibrated — the LAST liquidity placeholders. notional={:.0f} (reproducible: "
        "backing_asset_mv x illiquid_share x forced_sale_fraction); couplings "
        "(rate,equity,spread,lapse,mortality,fx)=({:.3f},{:.3f},{:.3f},{:.3f},{:.3f},{:.3f}) "
        "recovered within +/-{:.2f} of documented targets from 240 monthly joint observations; "
        "7x7 PSD-validated; var-covar SCR sensitivity bounded. Residual (keeps the risk open): "
        "nested/copula re-aggregation with these values is Task 4; documented-targets fixture is "
        "educational — credentialled joint data + independent APS X2 review required.".format(
            exposure["exposure_notional"],
            *(float(estimated[k]) for k in COUPLING_KEYS), 0.12)
    )
    entry.update_mitigation(MitigationStatus.MITIGATED, notes=note)
    store.audit_trail.append(
        AuditEntry.governance(
            actor=ACTOR, phase=PHASE,
            event="{} refreshed to MITIGATED following G-LIQX PASS "
                  "(liquidity exposure + coupling calibration)".format(risk_id),
            details={
                "risk_id": risk_id,
                "new_status": MitigationStatus.MITIGATED.value,
                "gate": "G-LIQX", "gate_status": gate.status,
                "exposure_notional": round(exposure["exposure_notional"], 2),
                "couplings": {k: round(float(estimated[k]), 4) for k in COUPLING_KEYS},
                "timestamp": ts,
            },
        )
    )
    return entry.mitigation_status.value


