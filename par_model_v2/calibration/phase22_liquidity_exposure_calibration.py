@dataclass
class LiquidityExposureCheck:
    market: str
    n_obs: int
    exposure_notional: float
    estimated_couplings: Dict[str, float]
    target_couplings: Dict[str, float]
    is_placeholder: bool
    criteria: Dict[str, bool] = field(default_factory=dict)

    def all_pass(self) -> bool:
        return bool(self.criteria) and all(self.criteria.values())


def check_exposure_calibration(spec: Dict[str, Any],
                               lineage: DataLineageRecord,
                               exposure: Dict[str, float],
                               estimated: Dict[str, float],
                               corr_report: Any,
                               sensitivity: Dict[str, Any],
                               has_param_change_audit: bool) -> LiquidityExposureCheck:
    """Score the six G-LIQX criteria."""
    tol = float(spec["coupling_tolerance"])
    targets = {k: float(spec["target_couplings"][k]) for k in COUPLING_KEYS}
    band = spec["exposure_inputs"]["notional_band"]
    n_obs = int(spec["monthly_synthesis"]["n_months"])
    notional = float(exposure["exposure_notional"])
    recovered = all(abs(estimated[k] - targets[k]) <= tol for k in COUPLING_KEYS)
    criteria = {
        "c1_lineage_checksum_ok": bool(lineage.sha256_checksum) and len(lineage.sha256_checksum) == 64,
        "c2_exposure_reproducible_in_band": (
            abs(notional - exposure["backing_asset_mv"] * exposure["illiquid_share"]
                * exposure["forced_sale_fraction"]) < 1e-9
            and float(band[0]) <= notional <= float(band[1])
        ),
        "c3_couplings_recovered_within_tol": recovered and n_obs >= int(spec["min_obs"]),
        "c4_correlation7_psd_validator_pass": bool(corr_report.passed),
        "c5_sensitivity_bounded": (
            sensitivity["calibrated_vs_placeholder_rel_change"]
            <= sensitivity["max_allowed_rel_change"]
            and sensitivity["grid_spread_rel"] <= sensitivity["max_allowed_rel_change"]
            and sensitivity["max_perturbation_rel_change"]
            <= sensitivity["max_allowed_rel_change"]
        ),
        "c6_not_placeholder_with_audit": has_param_change_audit,
    }
    return LiquidityExposureCheck(
        market=MARKET, n_obs=n_obs, exposure_notional=notional,
        estimated_couplings=dict(estimated), target_couplings=targets,
        is_placeholder=False, criteria=criteria,
    )


def evaluate_gliqx_gate(check: LiquidityExposureCheck) -> ProductionGateStatus:
    failed = [k for k, ok in check.criteria.items() if not ok]
    status = "PASS" if not failed else "FAIL"
    evidence = "{}: n={}, notional={:.0f}, couplings={}".format(
        check.market, check.n_obs, check.exposure_notional,
        {k: round(v, 4) for k, v in check.estimated_couplings.items()},
    )
    if failed:
        evidence += "; failed " + ", ".join(failed)
    return ProductionGateStatus(
        gate_id="G-LIQX",
        gate_description=("Liquidity exposure notional reproducible from documented "
                          "balance-sheet inputs and 7x7 liquidity couplings calibrated "
                          "(recovered within tolerance, PSD, bounded SCR sensitivity)"),
        status=status,
        evidence=evidence,
    )


