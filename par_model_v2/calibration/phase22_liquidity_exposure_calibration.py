def _target_matrix(spec: Dict[str, Any]) -> np.ndarray:
    six = SixDriverFXCorrelation()
    C = np.eye(7, dtype=float)
    C[:6, :6] = six.matrix(float(spec["gbm_rate_equity_corr"]))
    liq = np.array([float(spec["target_couplings"][k]) for k in COUPLING_KEYS])
    C[6, :6] = liq
    C[:6, 6] = liq
    return C


def synthesize_joint_panel(spec: Dict[str, Any]) -> Dict[str, np.ndarray]:
    """Seeded joint monthly panel: six innovation series + a CIR++ liquidity path.

    The liquidity path is generated with the full-truncation Euler CIR++
    scheme at the Phase 21 Task 3 G-LIQ calibrated parameters, driven by the
    seventh correlated innovation column.
    """
    syn = spec["monthly_synthesis"]
    n = int(syn["n_months"])
    dt = 1.0 / float(syn.get("steps_per_year", 12))
    rng = np.random.default_rng(int(syn["seed"]))

    C = _target_matrix(spec)
    try:
        L = np.linalg.cholesky(C)
    except np.linalg.LinAlgError as exc:  # documented targets must be PD
        raise ValueError("target 7x7 correlation is not PD") from exc
    Z = rng.standard_normal((n, 7)) @ L.T

    p = calibrated_liquidity_params()
    kappa = float(p.mean_reversion_speed)
    sigma = float(p.premium_vol)
    shift = float(p.shift)
    theta = float(p.long_run_premium_p) - shift          # CIR core long-run
    x = np.empty(n + 1, dtype=float)
    x[0] = float(p.initial_premium) - shift              # CIR core start
    for t in range(n):
        xp = max(x[t], 0.0)
        x[t + 1] = x[t] + kappa * (theta - xp) * dt + sigma * math.sqrt(xp * dt) * Z[t, 6]
    return {
        "innovations": Z[:, :6],          # rate, equity, credit, lapse, mortality, fx
        "liquidity_core_path": x,         # CIR core (premium = core + shift)
        "dt": np.array([dt]),
    }


def estimate_couplings(panel: Dict[str, np.ndarray]) -> Dict[str, float]:
    """Recover the liquidity couplings from the PATH via CIR transition residuals."""
    p = calibrated_liquidity_params()
    kappa = float(p.mean_reversion_speed)
    sigma = float(p.premium_vol)
    theta = float(p.long_run_premium_p) - float(p.shift)
    dt = float(panel["dt"][0])
    x = panel["liquidity_core_path"]
    xp = np.maximum(x[:-1], 0.0)
    denom = sigma * np.sqrt(np.maximum(xp, 1e-12) * dt)
    resid = (x[1:] - x[:-1] - kappa * (theta - xp) * dt) / denom
    Z6 = panel["innovations"]
    out: Dict[str, float] = {}
    for j, key in enumerate(COUPLING_KEYS):
        zj = Z6[:, j]
        if float(np.std(zj)) < 1e-12 or float(np.std(resid)) < 1e-12:
            out[key] = 0.0
        else:
            out[key] = float(np.corrcoef(resid, zj)[0, 1])
    return out


