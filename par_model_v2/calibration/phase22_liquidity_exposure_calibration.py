def run_phase22_liquidity_exposure_calibration(
    fixture_path: Optional[Path] = None,
    governance_store: Optional[GovernanceStore] = None,
    store_path: str = ".claude-dev/GOVERNANCE_STORE.json",
    task4_report_path: Optional[Path] = None,
    write_report: bool = False,
    docs_dir: str = "docs/validation",
    persist_governance: bool = False,
) -> Dict[str, Any]:
    """Full Phase 22 Task 3 pipeline; returns the report dict."""
    ts = datetime.now(timezone.utc).isoformat()

    if governance_store is None and os.path.exists(store_path):
        try:
            governance_store = GovernanceStore.from_json(
                open(store_path, encoding="utf-8").read())
        except Exception:  # noqa: BLE001
            governance_store = GovernanceStore()
    if governance_store is None:
        governance_store = GovernanceStore()

    spec, lineage = load_exposure_fixture(fixture_path)
    exposure = derive_exposure_notional(spec)
    panel = synthesize_joint_panel(spec)
    estimated = estimate_couplings(panel)
    corr, corr_report = build_calibrated_correlation(spec, estimated)
    sensitivity = var_covar_sensitivity(spec, corr, exposure["exposure_notional"],
                                        task4_report_path=task4_report_path)

    existing = [r for r in governance_store.change_records
                if r.title.startswith("MR-011/MR-012: Liquidity exposure notional")]
    if existing:
        cr = existing[0]
    else:
        cr = build_exposure_change_record(exposure, estimated, sensitivity)
        cr = submit_to_owner_review(cr)
        governance_store.add_change_record(cr)
    audit_entry = _build_param_change_entry(exposure, estimated, cr.record_id)
    governance_store.audit_trail.append(audit_entry)

    has_audit = any(
        e.details.get("parameter_name") == "liquidity_exposure_and_couplings[{}]".format(MARKET)
        for e in governance_store.audit_trail.filter_by_type(audit_entry.entry_type)
    )
    check = check_exposure_calibration(spec, lineage, exposure, estimated,
                                       corr_report, sensitivity, has_audit)
    gate = evaluate_gliqx_gate(check)

    mr011 = _refresh_risk(governance_store, MR_PROXY_ID, gate, exposure, estimated, ts)
    mr012 = _refresh_risk(governance_store, MR_DRIVER_ID, gate, exposure, estimated, ts)
    audit_ok = governance_store.audit_trail.verify_all()

    report: Dict[str, Any] = {
        "run_timestamp": ts,
        "phase": PHASE,
        "task": "Task 3 — Liquidity exposure-notional + coupling calibration (G-LIQX)",
        "market": MARKET,
        "lineage": lineage.to_dict(),
        "exposure": exposure,
        "target_couplings": {k: float(spec["target_couplings"][k]) for k in COUPLING_KEYS},
        "estimated_couplings": {k: round(float(estimated[k]), 6) for k in COUPLING_KEYS},
        "coupling_tolerance": float(spec["coupling_tolerance"]),
        "n_obs": int(spec["monthly_synthesis"]["n_months"]),
        "correlation7_psd_passed": bool(corr_report.passed),
        "sensitivity": sensitivity,
        "gate_gliqx": gate.to_dict(),
        "criteria": check.criteria,
        "is_placeholder": False,
        "change_record_id": cr.record_id,
        "change_record_status": cr.status.value,
        "mr011_status": mr011,
        "mr012_status": mr012,
        "audit_entry_ids": [audit_entry.entry_id],
        "audit_integrity_ok": bool(audit_ok),
        "change_records_total": len(governance_store.change_records),
        "use_restrictions": liquidity_exposure_use_restrictions(),
    }
    report["markdown"] = build_markdown(report)

    if write_report:
        ddir = Path(docs_dir)
        ddir.mkdir(parents=True, exist_ok=True)
        (ddir / "PHASE22_TASK3_LIQUIDITY_EXPOSURE_REPORT.json").write_text(
            json.dumps(report, indent=2), encoding="utf-8")
        (ddir / "PHASE22_TASK3_LIQUIDITY_EXPOSURE_REPORT.md").write_text(
            report["markdown"], encoding="utf-8")
    if persist_governance:
        with open(store_path, "w", encoding="utf-8") as fh:
            fh.write(governance_store.to_json())
    return report


def build_markdown(r: Dict[str, Any]) -> str:
    ts = r["run_timestamp"][:19].replace("T", " ") + " UTC"
    g = r["gate_gliqx"]
    icon = "PASS ✅" if g["status"] == "PASS" else "FAIL ❌"
    rows = "\n".join(
        "| {} | {:+.4f} | {:+.4f} | {:+.4f} |".format(
            k, r["target_couplings"][k], r["estimated_couplings"][k],
            r["estimated_couplings"][k] - r["target_couplings"][k])
        for k in COUPLING_KEYS
    )
    crit = "\n".join("| {} | {} |".format(k, "PASS" if v else "FAIL")
                     for k, v in r["criteria"].items())
    s = r["sensitivity"]
    grid = ", ".join("{}x -> {:.0f}".format(m, v)
                     for m, v in zip(s["notional_multipliers"], s["var_covar_scr_grid"]))
