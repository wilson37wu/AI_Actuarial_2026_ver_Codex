"""
Five-Driver Capital Re-Aggregation with the Two-Factor G2++ Rates Driver
========================================================================

Phase 20 Task 4 (capital re-aggregation half).  Re-runs the Phase 19 Task 4
five-driver tail-dependent aggregation
(:mod:`par_model_v2.projection.multi_driver_capital_5d_aggregation`) with the
**two-factor G2++** rates driver of Phase 20 Tasks 1-3 substituted for the
single-factor Hull-White (HW1F) rate driver in the OUTER real-world state, so
the rate-risk marginal that feeds economic capital now carries a genuine
second (slope / curvature) factor.

What this module is, and is NOT
-------------------------------
* It is **additive**: it imports, but never modifies, the five-driver nested
  primitives, the HW1F-based inner valuation, the var-covar / copula
  aggregation, and the Phase 20 calibrated G2++ parameters.
* The **OUTER** rate state ``r_H`` is generated by the calibrated G2++ two-factor
  short rate ``r(t) = phi(t) + x(t) + y(t)`` (exact-OU factor recursions), anchored
  to the SAME initial risk-free curve as the HW1F baseline, so the only thing that
  changes between the HW1F and G2++ runs is the *rate driver dynamics* — the
  comparison isolates the capital impact of the second factor + the swaption-
  calibrated factor volatilities/correlation.
* The **INNER** conditional valuation ``L(r_H, S_H, s_H, b_H, m_H)`` is reused
  verbatim from the five-driver engine: it conditions the governed HW1F Q nest at
  the realised ``r_H``.  In other words the G2++ driver enters the *outer
  real-world rate marginal and its tail*; the conditional liability *operator*
  ``L(.)`` is the governed nested operator.  This is the standard real-world-outer
  / risk-neutral-inner economic-capital decomposition (ASOP 56 §3.5).  Surfacing a
  fully G2++-consistent inner nest is recorded as a residual (Task 5 / future work).

Cross-driver correlation
-------------------------
The governed 5x5 ESG correlation couples the *rate* axis to equity / credit /
lapse / mortality.  With the two-factor driver the dominant factor ``x`` inherits
that governed cross-correlation (it is driven by the same correlated rate shock
``z_rate`` as the HW1F baseline), while the second factor ``y`` is correlated to
``x`` by the calibrated G2++ factor correlation ``rho`` and is otherwise
orthogonal to the four non-rate drivers.  This injects the second factor as a new
orthogonal curve-shape tail axis without disturbing the governed ESG block — a
documented, conservative modelling choice (ASOP 25 §3.3; IFoA Aggregation WP).

SOA ASOP 56 §3.5; SOA ASOP 56 §3.1; SOA ASOP 25 §3.3; SOA ASOP 7 §3.3;
IA TAS M §3.2/§3.5/§3.6; Solvency II Delegated Reg. Art. 234; Brigo-Mercurio (2006);
IFoA Life Aggregation & Simulation WP.
"""

