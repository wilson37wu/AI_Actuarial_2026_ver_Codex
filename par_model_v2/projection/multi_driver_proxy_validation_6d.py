@dataclass
class _FittedHexSurface:
    """A fitted six-driver capital surface in either fx mode.

    ``fx_mode == "learned"`` — ``beta`` spans the hexavariate basis over all
    six state variables and the prediction is the polynomial alone.
    ``fx_mode == "analytic"`` — ``beta`` spans the QUINTIVARIATE basis over
    the five stochastic-valuation drivers; the CIP-exact FX leg ``fx_l(X_H)``
    is added as a known offset by the validator (control-variate design).
    """

    beta: np.ndarray
    centers: np.ndarray
    scales: np.ndarray
    degree: int
    max_interaction_order: int
    fx_mode: str
    in_sample_r2_noisy: float

    @property
    def n_basis_terms(self) -> int:
        if self.fx_mode == "analytic":
            return _n_quint_basis_terms(self.degree, self.max_interaction_order)
        return _n_hex_basis_terms(self.degree, self.max_interaction_order)

    def predict_poly(self, X6: np.ndarray) -> np.ndarray:
        """Polynomial part only (no FX offset), from FULL (n, 6) states."""
        X6 = np.asarray(X6, dtype=float)
        if X6.ndim == 1:
            X6 = X6.reshape(1, -1)
        if self.fx_mode == "analytic":
            X = X6[:, :5]
            Xs = (X - self.centers) / self.scales
            return _quint_poly_basis(Xs, self.degree, self.max_interaction_order) @ self.beta
        Xs = (X6 - self.centers) / self.scales
        return _hex_poly_basis(Xs, self.degree, self.max_interaction_order) @ self.beta


def _fit_hex_surface(
    fit_X6: np.ndarray,
    fit_y5: np.ndarray,
    fx_l: np.ndarray,
    degree: int,
    max_interaction_order: int,
    fx_mode: str = "learned",
) -> _FittedHexSurface:
    """Least-squares fit of the six-driver ``(degree, max_int)`` surface.

    Same centring/scaling and ``np.linalg.lstsq`` protocol as the governed
    lower-dimensional engines, with the fitting data passed in so every basis
    is fitted on the *same* states + payoffs (comparable model selection).
    ``fit_y5`` is the noisy five-driver target and ``fx_l`` the exact FX leg:
    "learned" fits ``fit_y5 + fx_l`` on the hexavariate basis; "analytic" fits
    ``fit_y5`` on the quintivariate basis (FX handled as a known offset).
    """
    if fx_mode not in ("analytic", "learned"):
        raise ValueError("fx_mode must be 'analytic' or 'learned'")
    fit_X6 = np.asarray(fit_X6, dtype=float)
    if fit_X6.ndim != 2 or fit_X6.shape[1] != 6:
        raise ValueError("fit_X6 must have shape (n, 6)")
    if fx_mode == "analytic":
        X = fit_X6[:, :5]
        y = np.asarray(fit_y5, dtype=float)
        basis_fn = _quint_poly_basis
    else:
        X = fit_X6
        y = np.asarray(fit_y5, dtype=float) + np.asarray(fx_l, dtype=float)
        basis_fn = _hex_poly_basis
    centers = X.mean(axis=0)
    scales = X.std(axis=0, ddof=0)
    scales = np.where(scales > 0, scales, 1.0)
    Xs = (X - centers) / scales
    design = basis_fn(Xs, degree, max_interaction_order)
    beta, _resid, _rank, _sv = np.linalg.lstsq(design, y, rcond=None)
    y_hat = design @ beta
    ss_res = float(np.sum((y - y_hat) ** 2))
    ss_tot = float(np.sum((y - y.mean()) ** 2)) or 1.0
    return _FittedHexSurface(
        beta=beta, centers=centers, scales=scales, degree=int(degree),
        max_interaction_order=int(max_interaction_order), fx_mode=fx_mode,
        in_sample_r2_noisy=1.0 - ss_res / ss_tot,
    )


