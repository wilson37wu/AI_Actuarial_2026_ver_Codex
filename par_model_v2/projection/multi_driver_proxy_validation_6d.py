"""
Phase 21 Task 2 — Six-Driver Out-of-Sample Proxy Validation (FX included)
==========================================================================

Out-of-sample validation of a SIX-driver (G2++ short rate, equity, credit
spread, dynamic lapse, mortality trend, **FX translation**) LSMC economic-
capital proxy surface against high-accuracy nested truth on an independent,
disjoint-seed hold-out — completing the validation deliberately deferred from
Phase 21 Task 1 ("the nested benchmark is the only validated 6D ground truth
until Task 2 reports").

Methodology (mirrors the governed two-/three-/four-/five-driver validators)
----------------------------------------------------------------------------
* **State** ``x = (r_H, S_H, s_H, b_H, m_H, X_H)`` at the capital horizon,
  generated by the Phase 21 Task 1 six-driver outer construction
  (:meth:`SixDriverFXRiskAggregator._outer_states_6d`): G2++ 2F rates, GBM
  equity, CIR++ credit, OU lapse, OU mortality, lognormal FX, all off ONE
  shared 6-factor Cholesky-correlated antithetic draw under P.
* **Conditional liability decomposition** — the FX inner conditioning is
  analytic and CIP-exact (Phase 20 ``MART-FX-CIP``; Task 1), so::

      L6(x) = E^Q[L5 | r_H, S_H, s_H, b_H, m_H] + fx_l(X_H)

  with ``fx_l(X) = notional * (1 - X / X0)`` exact (no inner FX noise).  The
  noisy fitting target is one inner Q-path of L5 plus the exact FX term; the
  heavy hold-out truth is an ``n_inner_heavy``-path nested mean of L5 plus the
  exact FX term.  The five-driver inner nest is the governed HW1F nest
  conditioned at the realised G2++ ``r_H`` (Phase 20 documented residual).
* **Basis selection** sweeps a ``(degree, max_interaction_order)`` grid in TWO
  fx modes, all fitted on the SAME states + payoffs and selected jointly by
  OOS RMSE (or OOS R²) on the disjoint-seed hold-out:

  - ``"learned"`` — a hexavariate basis over all six state variables; the FX
    axis must be *estimated* from the noisy single-path fitting targets.
  - ``"analytic"`` — the CIP-exact FX leg is used as a known OFFSET (control
    variate): the polynomial spans only the five stochastic-valuation drivers
    and ``fx_l(X_H)`` is added back exactly in prediction.  This is the
    production-sensible design when part of the liability has a closed form
    (IFoA proxy-modelling WP structure-exploitation discipline).

  The head-to-head sweep documents the finding empirically: the learned FX
  coefficient has standard error ~``sigma_noise / (sqrt(n_fit) * sd(X_H))``,
  the same order as the true CIP slope at educational scale, so the offset
  mode is expected to dominate OOS — measured, not assumed.
* **Leakage + overfit diagnostics** — exact-state-sharing and min-distance
  evidence (`_leakage_nd`), in-sample(heavy) vs OOS R² gap per basis, and the
  overfit onset in basis-complexity order.
* **Capital comparison** — selected-surface proxy capital vs an independent
  six-driver nested benchmark evaluated on the SAME eval outer states
  (removes outer sampling noise from the proxy-vs-nested comparison; the
  remaining error is pure surface error — documented choice).

Staged execution (sandbox wall-clock discipline)
------------------------------------------------
``single_path_payoffs_sliced`` / ``heavy_targets_sliced`` spawn the FULL
per-state seed set (``SeedSequence(seed).spawn(n_full)``) and slice it, so a
staged run is bit-identical to a monolithic one (ASOP 56 reproducibility;
regression-tested).  ``validate(precomputed=...)`` consumes staged arrays.

EDUCATIONAL MODEL: placeholder parameters; not for production capital.

SOA ASOP 7 §3.3; ASOP 25 §3.3; ASOP 56 §3.1.3/§3.5; IA TAS M §3.2/§3.6;
IFoA proxy-modelling working party; Longstaff & Schwartz (2001).
"""

