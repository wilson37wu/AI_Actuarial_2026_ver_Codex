#: verifies that empirically rather than assuming it.
DEFAULT_HEX_BASIS_GRID: Tuple[Tuple[int, int], ...] = (
    (1, 3), (2, 3), (3, 2), (3, 3), (4, 3),
)


# ---------------------------------------------------------------------------
# Hexavariate polynomial basis with capped interaction order
# ---------------------------------------------------------------------------

def _hex_poly_powers(
    degree: int, max_interaction_order: int = 3
) -> List[Tuple[int, int, int, int, int, int]]:
    """All exponent tuples (a,b,c,d,e,f) with total <= degree, where terms
    involving >= 3 distinct drivers are admitted only up to
    ``max_interaction_order`` total degree (IFoA basis discipline)."""
    if degree < 0:
        raise ValueError("degree must be >= 0")
    powers: List[Tuple[int, int, int, int, int, int]] = []
    for total in range(degree + 1):
        for a in range(total + 1):
            for b in range(total - a + 1):
                for c in range(total - a - b + 1):
                    for d in range(total - a - b - c + 1):
                        for e in range(total - a - b - c - d + 1):
                            f = total - a - b - c - d - e
                            nonzero = sum(
                                1 for ex in (a, b, c, d, e, f) if ex >= 1
                            )
                            if nonzero >= 3 and total > max_interaction_order:
                                continue
                            powers.append((a, b, c, d, e, f))
    return powers


def _hex_poly_basis(
    X: np.ndarray, degree: int, max_interaction_order: int = 3
) -> np.ndarray:
    """Hexavariate design matrix for X of shape (n, 6)."""
    X = np.asarray(X, dtype=float)
    if X.ndim != 2 or X.shape[1] != 6:
        raise ValueError("X must have shape (n, 6); got {}".format(X.shape))
    powers = _hex_poly_powers(degree, max_interaction_order)
    cols = [
        X[:, 0] ** a * X[:, 1] ** b * X[:, 2] ** c
        * X[:, 3] ** d * X[:, 4] ** e * X[:, 5] ** f
        for (a, b, c, d, e, f) in powers
    ]
    return np.column_stack(cols)


def _n_hex_basis_terms(degree: int, max_interaction_order: int = 3) -> int:
    return len(_hex_poly_powers(degree, max_interaction_order))


