#: Default (degree, max_interaction_order) grid swept for the six-driver basis.
#: Mirrors the governed five-driver grid: the interaction cap is a genuine
#: complexity lever with six drivers, while the CIP-exact FX leg is exactly
#: LINEAR in X_H and additively separable — so a low-degree basis containing
