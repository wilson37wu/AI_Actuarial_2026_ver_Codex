class SixDriverFXProxyValidator:
    """Orchestrate fit / hold-out / basis-selection for the 6-driver proxy.

    Composes the Phase 21 Task 1 :class:`SixDriverFXRiskAggregator` for the
    governed six-driver outer-state construction and the FX exposure mapping;
    the five-driver inner Q-nest valuation (`_inner_pathwise_pvs_5d`) and the
    CIP-exact analytic FX term reproduce the Task 1 conditional liability
    exactly.  All heavy kernels expose ``*_sliced`` forms whose staged
    execution is bit-identical to a monolithic run.

    SOA ASOP 7 §3.3; ASOP 25 §3.3; ASOP 56 §3.5; IA TAS M §3.6; IFoA WP.
    """

    def __init__(
        self,
        product: ParEndowmentProduct,
        fx_params: Optional[FXParams] = None,
        fx_exposure: Optional[FXExposureSpec] = None,
        correlation6: Optional[SixDriverFXCorrelation] = None,
        capital_horizon_months: int = DEFAULT_CAPITAL_HORIZON_MONTHS,
        confidence_level: float = DEFAULT_CONFIDENCE_LEVEL,
        outer_measure: Measure = Measure.P,
        annual_qx_fn: Optional[Callable] = None,
        **aggregator_kwargs,
    ) -> None:
        if not (0 < capital_horizon_months < product.term_months):
            raise ValueError("capital_horizon_months must satisfy 0 < H < term_months")
        self.agg = SixDriverFXRiskAggregator(
            product,
            fx_params=fx_params,
            fx_exposure=fx_exposure,
            correlation6=correlation6,
            **aggregator_kwargs,
        )
        self.product = product
        self.capital_horizon_months = int(capital_horizon_months)
        self.confidence_level = float(confidence_level)
        self.outer_measure = Measure(outer_measure)
        self.annual_qx_fn = annual_qx_fn

    @property
    def _rem(self) -> int:
        return self.product.term_months - self.capital_horizon_months

    # -- state generation --------------------------------------------------- #
    def states(self, n: int, seed: int) -> np.ndarray:
        """(n, 6) outer states (r_H, S_H, s_H, b_H, m_H, X_H) — Task 1 kernel."""
        return self.agg._outer_states_6d(
            n, self.capital_horizon_months, self.outer_measure, seed
        )

    # -- analytic FX term (CIP-exact; no staging needed) --------------------- #
    def fx_term(self, states6: np.ndarray) -> np.ndarray:
        return self.agg.fx_exposure.liability_impact(
            np.asarray(states6, dtype=float)[:, 5]
        )

    # -- 5D inner kernels, slice-stable CRN ---------------------------------- #
    def _pvs_5d(self, row: np.ndarray, n_inner: int, inner_seed: int) -> np.ndarray:
        r, s, c, b, m = (float(v) for v in row[:5])
        return _inner_pathwise_pvs_5d(
            r, s, c, b, m, n_inner, self._rem,
            self.product, self.agg.hw_params, self.agg.gbm_params,
            self.agg.spread_params, self.agg.correlation,
            self.capital_horizon_months, inner_seed,
            self.agg.equity_guarantee, self.agg.credit_exposure,
            self.agg.lapse_exposure, self.agg.mortality_exposure,
            self.annual_qx_fn,
        )

    def single_path_payoffs_sliced(
        self, states6_full: np.ndarray, i0: int, i1: int, seed: int
    ) -> np.ndarray:
        """Noisy 5D single-inner-path payoffs for FULL-array rows [i0, i1).

        Spawns the FULL per-state seed set (``SeedSequence(seed + 1)``) and
        slices it, so any staging reproduces the monolithic values bit-for-bit.
        The exact FX term is NOT included (added analytically in validate()).
        """
        child = np.random.SeedSequence(seed + 1).spawn(len(states6_full))[i0:i1]
        y = np.empty(i1 - i0, dtype=float)
        for j in range(i1 - i0):
            inner_seed = int(child[j].generate_state(1)[0])
            y[j] = float(self._pvs_5d(states6_full[i0 + j], 1, inner_seed)[0])
        return y

    def heavy_targets_sliced(
        self, states6_full: np.ndarray, i0: int, i1: int, n_inner: int, seed: int
    ) -> np.ndarray:
        """High-accuracy nested E^Q[L5 | x] for FULL-array rows [i0, i1).

        Same slice-stable CRN protocol; FX term excluded (analytic, exact).
        """
        child = np.random.SeedSequence(seed).spawn(len(states6_full))[i0:i1]
        truth = np.empty(i1 - i0, dtype=float)
        for j in range(i1 - i0):
            inner_seed = int(child[j].generate_state(1)[0])
            truth[j] = float(self._pvs_5d(states6_full[i0 + j], n_inner, inner_seed).mean())
        return truth

    # -- main workflow -------------------------------------------------------- #
    def validate(
        self,
        config: Optional[HexProxyValidationConfig] = None,
        precomputed: Optional[Dict[str, np.ndarray]] = None,
        governance_store: Optional["object"] = None,
        actor: str = "SixDriverFXProxyValidator",
        phase: str = "Phase 21: FX + Liquidity Drivers and Six/Seven-Driver Economic Capital",
    ) -> HexProxyValidationReport:
        """Run the full disjoint-seed hold-out workflow.

        ``precomputed`` (staged execution) may supply any of the four heavy 5D
        arrays — ``fit_y5`` (n_fit), ``val_truth5`` (n_validation),
        ``insample_truth5`` (n_insample_heavy), ``nested_l5`` (n_eval) — each
        produced by the ``*_sliced`` kernels over the corresponding state set;
        missing keys are computed monolithically (bit-identical either way).
        """
        cfg = config or HexProxyValidationConfig()
        pre = precomputed or {}
        t0 = time.monotonic()
        run_id = "hx-proxyval-" + uuid.uuid4().hex[:8]
        notes: List[str] = []

        # --- 1. states (deterministic, cheap — always regenerated) ----------
        fit_X = self.states(cfg.n_fit, cfg.fit_seed)
        val_X = self.states(cfg.n_validation, cfg.validation_seed)
        eval_X = self.states(cfg.n_eval, cfg.eval_seed)

        # --- 2. fitting data: noisy 5D single path + exact FX term ----------
        fit_y5 = (
            np.asarray(pre["fit_y5"], dtype=float) if "fit_y5" in pre
            else self.single_path_payoffs_sliced(fit_X, 0, cfg.n_fit, cfg.fit_seed)
        )
        if len(fit_y5) != cfg.n_fit:
            raise ValueError("fit_y5 must have length n_fit")

        # --- 3. independent heavy hold-out (disjoint seed) -------------------
        val_truth5 = (
            np.asarray(pre["val_truth5"], dtype=float) if "val_truth5" in pre
            else self.heavy_targets_sliced(
                val_X, 0, cfg.n_validation, cfg.n_inner_heavy, cfg.validation_seed
            )
        )
        if len(val_truth5) != cfg.n_validation:
            raise ValueError("val_truth5 must have length n_validation")
        val_truth = val_truth5 + self.fx_term(val_X)

        # --- 4. held-in heavy subset (in/out skill gap) ----------------------
        n_in_heavy = min(cfg.n_insample_heavy, len(fit_X))
        insample_X = fit_X[:n_in_heavy]
        insample_truth5 = (
            np.asarray(pre["insample_truth5"], dtype=float) if "insample_truth5" in pre
            else self.heavy_targets_sliced(
                insample_X, 0, n_in_heavy, cfg.n_inner_heavy, cfg.insample_heavy_seed
            )
        )
        if len(insample_truth5) != n_in_heavy:
            raise ValueError("insample_truth5 must have length n_insample_heavy")
        insample_truth = insample_truth5 + self.fx_term(insample_X)

        # --- 5. leakage diagnostics ------------------------------------------
        leakage = _leakage_nd(fit_X, val_X, cfg.fit_seed, cfg.validation_seed)
        if not leakage.leakage_free:
            notes.append("WARNING: hold-out leakage check did not pass — review seeds.")

        # --- 6. basis sweep (both fx modes) on shared fitting data ------------
        fit_fx = self.fx_term(fit_X)
        val_fx = self.fx_term(val_X)
        insample_fx = self.fx_term(insample_X)

        def _predict_l6(surf: _FittedHexSurface, X6: np.ndarray, fx: np.ndarray) -> np.ndarray:
            poly = surf.predict_poly(X6)
            return poly + fx if surf.fx_mode == "analytic" else poly

        rows: List[HexBasisDiagnostics] = []
        surfaces: Dict[Tuple[str, int, int], _FittedHexSurface] = {}
        for mode in cfg.fx_modes:
            for (d, m) in cfg.basis_grid:
                surf = _fit_hex_surface(fit_X, fit_y5, fit_fx, d, m, fx_mode=mode)
                surfaces[(mode, d, m)] = surf
                val_pred = _predict_l6(surf, val_X, val_fx)
                in_pred = _predict_l6(surf, insample_X, insample_fx)
                resid = val_pred - val_truth
                denom = np.where(np.abs(val_truth) > 1e-9, np.abs(val_truth), 1.0)
                oos_rmse = float(np.sqrt(np.mean(resid ** 2)))
                oos_mae = float(np.mean(np.abs(resid)))
                oos_max_rel = float(np.max(np.abs(resid) / denom))
                oos_r2 = _r2(val_truth, val_pred)
                in_r2_heavy = _r2(insample_truth, in_pred)
                rows.append(HexBasisDiagnostics(
                    fx_mode=mode, degree=d, max_interaction_order=m,
                    n_basis_terms=surf.n_basis_terms,
                    in_sample_r2_noisy=surf.in_sample_r2_noisy,
                    in_sample_r2_heavy=in_r2_heavy,
                    oos_rmse=oos_rmse, oos_r2=oos_r2, oos_mae=oos_mae,
                    oos_max_abs_rel_error=oos_max_rel,
                    overfit_gap=in_r2_heavy - oos_r2,
                ))

        # --- 7. model selection by OOS skill (across modes AND bases) ---------
        if cfg.selection_metric == "oos_rmse":
            selected = min(rows, key=lambda r: r.oos_rmse)
        else:
            selected = max(rows, key=lambda r: r.oos_r2)
        selected_key = (selected.fx_mode, selected.degree, selected.max_interaction_order)

        # Overfit onset within the SELECTED mode (complexity ordering across
        # modes would mix non-nested bases).
        mode_rows = [r for r in rows if r.fx_mode == selected.fx_mode]
        ordered_mode = sorted(
            mode_rows, key=lambda r: (r.n_basis_terms, r.degree, r.max_interaction_order)
        )
        overfit_onset_terms: Optional[int] = None
        for prev, cur in zip(ordered_mode, ordered_mode[1:]):
            if cur.oos_rmse > prev.oos_rmse * 1.001:
                overfit_onset_terms = cur.n_basis_terms
                break
        ordered = sorted(
            rows, key=lambda r: (r.fx_mode, r.n_basis_terms, r.degree,
                                 r.max_interaction_order)
        )
        learned_best = min(
            (r for r in rows if r.fx_mode == "learned"),
            key=lambda r: r.oos_rmse, default=None,
        )
        analytic_best = min(
            (r for r in rows if r.fx_mode == "analytic"),
            key=lambda r: r.oos_rmse, default=None,
        )
        if learned_best is not None and analytic_best is not None:
            notes.append(
                "FX-mode head-to-head (best OOS RMSE): analytic offset {:.2f} vs "
                "learned hexavariate {:.2f} — the control-variate design {} the "
                "fully-learned FX axis at this educational scale.".format(
                    analytic_best.oos_rmse, learned_best.oos_rmse,
                    "dominates" if analytic_best.oos_rmse <= learned_best.oos_rmse
                    else "does NOT dominate")
            )

        # --- 8. capital comparison at the selected basis -----------------------
        chosen = surfaces[selected_key]
        eval_fx = self.fx_term(eval_X)
        proxy_l = _predict_l6(chosen, eval_X, eval_fx)
        proxy_capital = capital_metrics_from_liabilities(
            proxy_l, cfg.confidence_level, cfg.capital_horizon_months
        )
        nested_l5 = (
            np.asarray(pre["nested_l5"], dtype=float) if "nested_l5" in pre
            else self.heavy_targets_sliced(
                eval_X, 0, cfg.n_eval, cfg.nested_n_inner, cfg.nested_inner_seed
            )
        )
        if len(nested_l5) != cfg.n_eval:
            raise ValueError("nested_l5 must have length n_eval")
        nested_l = nested_l5 + self.fx_term(eval_X)
        nested_capital = capital_metrics_from_liabilities(
            nested_l, cfg.confidence_level, cfg.capital_horizon_months
        )
        notes.append(
            "Proxy and nested capital are evaluated on the SAME eval outer states "
            "(seed {}), so the comparison isolates pure surface error; the nested "
            "benchmark uses {} heavy inner Q-paths per state.".format(
                cfg.eval_seed, cfg.nested_n_inner)
        )

        def _rel(a: float, b: float) -> float:
            return abs(a - b) / (abs(b) if abs(b) > 1e-9 else 1.0)

        capital_cmp = CapitalComparison(
            proxy_capital=proxy_capital, nested_capital=nested_capital,
            var_rel_error=_rel(proxy_capital.var_liability, nested_capital.var_liability),
            es_rel_error=_rel(proxy_capital.es_liability, nested_capital.es_liability),
            scr_rel_error=_rel(proxy_capital.scr_proxy, nested_capital.scr_proxy),
            nested_n_outer=cfg.n_eval, nested_n_inner=cfg.nested_n_inner,
        )

        # --- 9. FX-axis recovery evidence --------------------------------------
        # The CIP-exact FX leg is exactly linear in X_H with slope
        # -notional / X0; verify the selected surface recovered it by linear
        # projection of the surface's partial FX effect on the hold-out.
        x0 = float(self.agg.fx_exposure.initial_spot_rate)
        notional = float(self.agg.fx_exposure.exposure_notional)
        theoretical_slope = -notional / x0
        x_col = val_X[:, 5]
        base = val_X.copy()
        base[:, 5] = x0
        base_fx = self.fx_term(base)  # exactly zero at X = X0
        partial_fx = _predict_l6(chosen, val_X, val_fx) - _predict_l6(chosen, base, base_fx)
        slope_fit = (
            float(np.polyfit(x_col - x0, partial_fx, 1)[0])
            if np.std(x_col) > 0 else float("nan")
        )
        fx_axis_evidence = {
            "theoretical_fx_slope": theoretical_slope,
            "recovered_fx_slope": slope_fit,
            "slope_rel_error": abs(slope_fit - theoretical_slope)
            / max(abs(theoretical_slope), 1e-9),
        }
        notes.append(
            "FX-axis recovery: surface partial-FX slope {:.2f} vs CIP-exact "
            "theoretical {:.2f} (rel err {:.2%}).".format(
                slope_fit, theoretical_slope, fx_axis_evidence["slope_rel_error"])
        )

        # --- 10. reproducibility digest ----------------------------------------
        digest = hashlib.sha256(
            np.round(np.concatenate([
                val_truth, chosen.beta,
                np.array([1.0 if selected.fx_mode == "analytic" else 0.0,
                          selected.degree, selected.max_interaction_order,
                          proxy_capital.var_liability, proxy_capital.es_liability],
                         dtype=float),
            ]), 9).tobytes()
        ).hexdigest()

        # --- 11. verdict ---------------------------------------------------------
        verdict = self._verdict(selected, capital_cmp, leakage, fx_axis_evidence, notes)

        duration = time.monotonic() - t0

        audit_entry_id = None
        if governance_store is not None:
            try:
                from par_model_v2.governance.audit_trail import AuditEntry

                entry = AuditEntry.model_run(
                    actor=actor, phase=phase, run_id=run_id,
                    scenario_count=cfg.n_fit
                    + (cfg.n_validation + n_in_heavy) * cfg.n_inner_heavy
                    + cfg.n_eval * cfg.nested_n_inner,
                    duration_seconds=round(duration, 4),
                    outcome="PASS" if verdict.startswith("PASS") else "PARTIAL",
                    files_changed=[
                        "par_model_v2/projection/multi_driver_proxy_validation_6d.py"
                    ],
                    test_summary=(
                        "6D OOS proxy-val: selected ({}, deg={}, max_int={}), OOS R2={:.4f}, "
                        "OOS RMSE={:.2f}, VaR rel err={:.2%}, leakage_free={}, "
                        "fx_slope_rel_err={:.2%}".format(
                            selected.fx_mode, selected.degree,
                            selected.max_interaction_order,
                            selected.oos_r2, selected.oos_rmse,
                            capital_cmp.var_rel_error, leakage.leakage_free,
                            fx_axis_evidence["slope_rel_error"])
                    ),
                )
                governance_store.audit_trail.append(entry)
                audit_entry_id = entry.entry_id
            except Exception as exc:  # pragma: no cover - governance optional
                notes.append("audit append skipped: {}".format(exc))

        return HexProxyValidationReport(
            config=cfg, basis_rows=ordered,
            selected_fx_mode=selected.fx_mode,
            selected_degree=selected.degree,
            selected_max_interaction_order=selected.max_interaction_order,
            selection_metric=cfg.selection_metric,
            overfit_onset_terms=overfit_onset_terms,
            leakage=leakage, capital_comparison=capital_cmp,
            fx_axis_evidence=fx_axis_evidence,
            reproducibility_digest=digest, run_id=run_id, duration_seconds=duration,
            verdict=verdict, notes=notes, audit_entry_id=audit_entry_id,
        )

    @staticmethod
    def _verdict(
        selected: HexBasisDiagnostics,
        capital_cmp: CapitalComparison,
        leakage: LeakageDiagnostics,
        fx_axis_evidence: Dict[str, float],
        notes: List[str],
    ) -> str:
        """Honest PASS / PARTIAL verdict against documented thresholds.

        Thresholds (educational; mirror the five-driver gate, plus the FX-axis
        recovery check): OOS R^2 >= 0.95, selected-basis VaR rel error <= 10%,
        hold-out leakage-free, overfit gap <= 0.05, and the surface's partial
        FX slope within 10% of the CIP-exact theoretical slope.
        """
        reasons: List[str] = []
        if selected.oos_r2 < 0.95:
            reasons.append("OOS R^2 {:.4f} < 0.95".format(selected.oos_r2))
        if capital_cmp.var_rel_error > 0.10:
            reasons.append("VaR rel error {:.2%} > 10%".format(capital_cmp.var_rel_error))
        if not leakage.leakage_free:
            reasons.append("hold-out not leakage-free")
        if selected.overfit_gap > 0.05:
            reasons.append("overfit gap {:.4f} > 0.05".format(selected.overfit_gap))
        if fx_axis_evidence["slope_rel_error"] > 0.10:
            reasons.append(
                "FX-axis slope rel error {:.2%} > 10%".format(
                    fx_axis_evidence["slope_rel_error"])
            )
        if reasons:
            notes.append("verdict drivers: " + "; ".join(reasons))
            return "PARTIAL — " + "; ".join(reasons)
        return (
            "PASS — selected basis (deg {}, max_int {}) validated OOS "
            "(R^2={:.4f}, VaR rel err={:.2%}, leakage-free, overfit gap={:.4f}, "
            "FX axis recovered within {:.2%})".format(
                selected.degree, selected.max_interaction_order, selected.oos_r2,
                capital_cmp.var_rel_error, selected.overfit_gap,
                fx_axis_evidence["slope_rel_error"])
        )


